def deep_research(ticker):
    report = {}

    df = load_features(ticker)
    latest = df.iloc[-1]

    trend = "UPTREND" if latest['MA50'] > latest['MA200'] else "DOWNTREND"
    rsi = round(latest['RSI'], 2)
    volatility = round(df['Return'].std() * 100, 2)
    volatility_str = f"{volatility}%"

    report['technical'] = {
        "trend": trend,
        "rsi": float(rsi),
        "volatility": volatility_str
    }

    company, macro = fetch_market_news(ticker)
    sent = weighted_sentiment(company, macro)

    prob = model_prediction(df, sent)

    company_news = []
    if company:
        for n in company[:3]:
            snippet = fetch_article_snippet(n["link"])
            company_news.append({
                "title": n['title'],
                "snippet": snippet,
                "link": n['link']
            })
    
    macro_news = []
    if macro:
        for n in macro[:3]:
            macro_news.append({
                "title": n['title'],
                "link": n['link']
            })

    report['news_analysis'] = {
        "company_specific": company_news if company_news else [],
        "market_macro": macro_news if macro_news else []
    }

    signal = "SIDEWAYS"
    if prob > 0.6:
        signal = "BULLISH SWING"
    elif prob < 0.4:
        signal = "BEARISH SWING"

    report['ai_probability'] = {
        "bullish_probability": f"{round(prob * 100, 2)}%",
        "signal": signal
    }

    price = latest['Close']
    report['swing_plan'] = {
        "entry": round(price, 2),
        "stoploss": round(price * 0.97, 2),
        "target": round(price * 1.05, 2)
    }

    return report

def scan_market(index_name="NIFTY 50", limit=None, top_n=10):
    stocks = get_index(index_name)

    if limit:
        stocks = stocks[:limit]

    results = []

    def check(stock):
        try:
            df = load_features(stock)
            if df is None or len(df) < 200:
                return

            company, macro = fetch_market_news(stock)
            sent = weighted_sentiment(company, macro)
            prob = model_prediction(df, sent)
            results.append((stock, round(prob * 100, 2)))
        except Exception as e:
            print(f"Error processing {stock}: {e}")

