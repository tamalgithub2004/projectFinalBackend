def model_prediction(df, sentiment):

    df['Target'] = (df['Return'].shift(-1) > 0.004).astype(int)

    features = df[['RSI','MACD','MACD_signal','MA50','MA200','VOL_MA']].copy()
    features.loc[:, 'sentiment'] = sentiment

    X = features[:-1]
    y = df['Target'][:-1]

    split = int(len(X)*0.8)

    model = RandomForestClassifier(
        n_estimators=300,
        random_state=42
    )

    model.fit(X[:split], y[:split])
    prob = model.predict_proba(X.iloc[-1:])[0][1]

    return prob

