def fetch_market_news(ticker):

    symbol = ticker.replace(".NS","")
    symbol_lower = symbol.lower()

    company = []
    macro = []

    for source, url in RSS_FEEDS.items():
        try:
            feed = feedparser.parse(url)

            for entry in feed.entries[:50]:

                title = entry.title.lower()
                summary = entry.get("summary", "").lower()

                # Check both title and summary
                if symbol_lower in title or symbol_lower in summary:
                    company.append({
                        "title": entry.title,
                        "link": entry.link
                    })

                elif any(k in title for k in HIGH_IMPACT):
                    macro.append({
                        "title": entry.title,
                        "link": entry.link
                    })

        except:
            continue

    return company, macro


def sentiment_score(news_list):

    if len(news_list) == 0:
        return 0

    scores=[]
    for n in news_list[:5]:
        inputs = tokenizer(n["title"], return_tensors="pt", truncation=True)
        out = model_nlp(**inputs)
        p = torch.softmax(out.logits, dim=1)[0].tolist()
        scores.append(p[2]-p[0])

    return np.mean(scores)


def weighted_sentiment(company, macro):
    comp = sentiment_score(company)
    mac = sentiment_score(macro)
    final = (0.7*comp) + (0.3*mac)
    return final

