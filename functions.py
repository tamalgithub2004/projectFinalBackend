def get_index(index_name="NIFTY 50"):

    try:
        headers = {"User-Agent": "Mozilla/5.0"}
        url = f"https://www.nseindia.com/api/equity-stockIndices?index={index_name.replace(' ','%20')}"
        data = requests.get(url, headers=headers, timeout=5).json()

        symbols = []

        for d in data["data"]:
            sym = d.get("symbol")

            # Skip only actual index rows
            if sym and sym != "NIFTY":
                symbols.append(sym + ".NS")

        return symbols

    except Exception:
        print("⚠ API fetch failed. Using fallback list.")
        return [
            "RELIANCE.NS","TCS.NS","INFY.NS","HDFCBANK.NS","ITC.NS",
            "LT.NS","SBIN.NS","AXISBANK.NS","ICICIBANK.NS","WIPRO.NS"
        ]

